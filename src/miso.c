TextureBatch* CreateTextureBatch(Texture *texture, int maxVertices) {
    sg_buffer_desc desc = {
        .usage = SG_USAGE_STREAM,
        .size = 6 * maxVertices * sizeof(Vertex)
    };
    TextureBatch *result = malloc(sizeof(TextureBatch));
    result->vertices = malloc(6 * maxVertices * sizeof(Vertex));
    result->maxVertices = maxVertices;
    result->vertexCount = 0;
    result->binding = (sg_bindings) {
            .fs_images[SLOT_sprite] = texture->texture,
            .vertex_buffers[0] = sg_make_buffer(&desc)
    };
    result->size = (Vector2){texture->w, texture->h};
    return result;
}

void TextureBatchDraw(TextureBatch *batch, Vector2 position, Vector2 size, Vector2 scale, Vector2 viewportSize, float rotation, Rectangle clip) {
    Vector2 quad[4] = {
        {position.x, position.y + size.y}, // bottom left
        {position.x + size.x, position.y + size.y}, // bottom right
        {position.x + size.x, position.y }, // top right
        {position.x, position.y }, // top left
    };
    float vw =  2.f / viewportSize.x;
    float vh = -2.f / viewportSize.y;
    for (int j = 0; j < 4; j++)
        quad[j] = (Vector2) {
            (vw * quad[j].x + -1.f) * scale.x,
            (vh * quad[j].y +  1.f) * scale.y
        };
    
    float iw = 1.f/batch->size.x, ih = 1.f/batch->size.y;
    float tl = clip.x*iw;
    float tt = clip.y*ih;
    float tr = (clip.x + clip.w)*iw;
    float tb = (clip.y + clip.h)*ih;
    Vector2 vtexquad[4] = {
        {tl, tb}, // bottom left
        {tr, tb}, // bottom right
        {tr, tt}, // top right
        {tl, tt}, // top left
    };
    
    static int indices[6] = {
        0, 1, 2,
        3, 0, 2
    };
    
    Vertex result[6];
    for (int i = 0; i < 6; i++)
        result[i] = (Vertex) {
            .position = quad[indices[i]],
            .texcoord = vtexquad[indices[i]]
        };
    memcpy(&batch->vertices[batch->vertexCount++ * 6], result, 6 * sizeof(Vertex));
}

void FlushTextureBatch(TextureBatch *batch) {
    if (!batch || !batch->vertexCount)
        return;
    sg_range range = {
        .ptr = batch->vertices,
        .size = 6 * batch->vertexCount * sizeof(Vertex)
    };
    sg_update_buffer(batch->binding.vertex_buffers[0], &range);
    sg_apply_bindings(&batch->binding);
    sg_draw(0, 6 * batch->vertexCount, 1);
    memset(batch->vertices, 0, 6 * batch->maxVertices * sizeof(Vertex));
    batch->vertexCount = 0;
}

void DestroyTextureBatch(TextureBatch *batch) {
    if (batch && sg_query_buffer_state(batch->binding.vertex_buffers[0]) != SG_RESOURCESTATE_INVALID) {
        sg_destroy_buffer(batch->binding.vertex_buffers[0]);
        if (batch->vertices)
            free(batch->vertices);
    }
}

typedef struct {
    const char *name;
    Texture *texture;
} TextureBucket;

static uint64_t HashBatch(const void *item, uint64_t seed0, uint64_t seed1) {
    TextureBucket *bucket = (TextureBucket*)item;
    return hashmap_sip(bucket->name, strlen(bucket->name), seed0, seed1);
}

static int CompareBatch(const void *a, const void *b, void*_) {
    TextureBucket *bucketA = (TextureBucket*)a;
    TextureBucket *bucketB = (TextureBucket*)b;
    return strcmp(bucketA->name, bucketB->name);
}

static void FreeBatch(void *item) {
    TextureBucket *bucket = (TextureBucket*)item;
    DestroyTexture(bucket->texture);
}

void TextureManagerAdd(const char *path) {
    TextureBucket search = {.name=path};
    TextureBucket *found = NULL;
    if ((found = hashmap_get(state.map, (void*)&search)))
        return;
    search.texture = LoadTextureFromFile(path);
    if (sg_query_image_state(search.texture->texture) != SG_RESOURCESTATE_INVALID)
        hashmap_set(state.map, (void*)&search);
}

Texture* TextureManagerGet(const char *path) {
    TextureBucket search = {.name=path};
    TextureBucket *found = NULL;
    if (!(found = hashmap_get(state.map, (void*)&search)))
        return NULL;
    return found->texture;
}

void ClearTextureManager(void) {
    size_t iter = 0;
    void *item;
    while (hashmap_iter(state.map, &iter, &item)) {
        TextureBucket *bucket = (TextureBucket*)item;
        DestroyTexture(bucket->texture);
    }
    hashmap_clear(state.map, true);
}

